package fr.unice.i3s.sigma.core;

@SuppressWarnings("serial")
public class SigmaQuickFixExecutionException extends Exception {

	public SigmaQuickFixExecutionException() {
		super();
	}

	public SigmaQuickFixExecutionException(String message, Throwable cause) {
		super(message, cause);
	}

	public SigmaQuickFixExecutionException(String message) {
		super(message);
	}

	public SigmaQuickFixExecutionException(Throwable cause) {
		super(cause);
	}
}